#operadores de asignacion
edad = 27

print(edad)

#para sumarle 5
edad += 5
print(edad)

#para restarle 5
edad -= 10
print(edad)


#OPERADORES DE INCREMENTO Y DECREMENTO
year = 2021

#incremento 
year = year + 1
print(year)

#decremento 
year = year - 1
print(year)


#pre incremento 
year = 1 + year 
print(year)

#pre decremento 
year = 1 - year 
print(year)